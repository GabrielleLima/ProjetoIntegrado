                 <h2>A Convenção Batista Brasileira</h2><br>
            <h4>Sua Origem</h4><br><br>

        Em 1882, quando foi organizada a Primeira Igreja Batista, voltada para a evangelização do Brasil, já existiam duas outras Igrejas Batistas, organizadas por imigrantes norte-americanos, 
        residentes na região de Santa Bárbara do D'Oeste e Americana, em São Paulo.<br><br>

Os casais de missionários Batistas norte-americanos, recém-chegados ao Brasil, Willian Buck Bagby e Anne Luther Bagby, os pioneiros, e Zacharias Clay Taylor, Kate Stevens Crawford Taylor, 
auxiliados pelo ex-padre Antônio Texeira de Albuquerque, batizado em Santa Bárbara D'Oeste, decidiram iniciar a sua missão na cidade de Salvador, Bahia, com 250.000 habitantes. 
Ali chegaram no dia 31 de agosto de 1882 e no dia 15 de outubro, organizaram a PIB do Brasil com 5 membros: os dois casais de missionárias e o ex-padre Antônio Teixeira.<br><br>

<h4> Este foi o início</h4> <br><br>
Nos primeiros vinte e cinco (25) anos de trabalho, Bagby e Taylor, auxiliados por outros missionários, e por um número crescente de brasileiros, evangelistas e pastores,
já tinham organizado 83 Igrejas, com aproximadamente 4.200 membros.<br><br>  

<h4>Organização da Convenção </h4> <br><br>

Segundo José dos Reis Pereira, Salomão Ginsburg foi a primeira pessoa a pensar na organização de uma Convenção Nacional dos Batistas Brasileiros.<br><br>

Mas, somente em 1907, a ideia foi concretizada. A. B. Deter, Zacharias Taylor e Salomão Ginsburg concordaram em dar prosseguimento ao plano. Eles conseguiram a adesão de outros
missionários e de líderes brasileiros, inclusive Francisco Fulgêncio Soren, que tinha, inicialmente, algumas reservas.<br><br>
A comissão organizadora optou pela data de 22 de junho de 1907 para organizar a Convenção, na cidade de Salvador, quando transcorreriam os primeiros 25 anos do início do trabalho Batista brasileiro, 
também iniciado na referida cidade.<br><br>
No dia aprazado, no prédio do ALJUBE, onde funcionava a PIB de Salvador, em sessão solene, foi realizada a primeira Assembleia da Convenção Batista Brasileira,
composta de 43 mensageiros enviados por Igrejas e organizações. A casa estava cheia. O clima era de festa, celebrando o que Deus fizera a partir daquele início tão pequeno!<br><br>

Criada a Convenção, foi eleita sua primeira diretoria: <br>
Presidente - Francisco Fulgêncio Soren; <br>

1º Vice-presidente - Joaquim Fernandes Lessa;<br>
2º Vice-presidente - João Borges da Rocha; <br>
1º Secretário - Teodoro Rodrigues Teixeira; <br>
2º Secretário - Manuel I. Sampaio; <br>
Tesoureiro - Zacharias Taylor;<br><br>

A motivação básica da criação da Convenção foi Missões, e falava-se na evangelização de Portugal,  Chile e África. Foram criadas, além das duas Juntas Missionárias, 
Missões Nacionais e Missões Estrangeiras (hoje Missões Mundiais) outras juntas: para a Casa Publicadora Batista, para Escola Bíblica Dominical, para União de Mocidade Batista, 
para Educação e Seminário, e para a Administração do Seminário. Ao todo, 7 Juntas.<br><br>

As áreas de Missões, Educação Religiosa e Publicações, Educação Teológica e Educação, foram as que receberam maior atenção dos convencionais.<br><br>